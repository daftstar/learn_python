x = [["a", "b", "c"],
     ["d", "e", "f"],
     ["g", "h", "i"]]

# Access 3rd dimension in list x
print (x[2])

# Access last dimension, 1st object in list x
print (x[-1][0])

# Access last dimension, first two objects in list x
print (x[2][:2])



